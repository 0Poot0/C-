//LOOPS******
  //FOR LOOP

#include<iostream>
using namespace std;
int main(){
    //for(initialisation,condition,updation)
    for(int i=0; i<=50; i++){
        cout<<i<<endl;
    }

//WHILE LOOP****8
// while(condition):
//while(true) for infinite loop....
    int i=0;
    while(i<=40){
        cout<<i<<endl;
        i++;

    }

    // DO WHILE LOOP....
    int a= 1;
    do{
        cout<<a<<endl;
        a++;
    }while(a<=20);


    return 0;
}